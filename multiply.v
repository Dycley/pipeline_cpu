module multiply(              // 乘法器
    input  [31:0] mult_op1,   // 乘法源操作数1
    input  [31:0] mult_op2,   // 乘法源操作数2
    output [63:0] product);    // 乘积
    assign product = mult_op1 * mult_op2;
endmodule

